/**
 * Generate src/lib/landingSnapshot.ts — REAL Strategy Lab backtest results,
 * snapshotted for the marketing landing page (no live API call per visit).
 *
 *   npm run snapshot:landing
 *
 * Runs each featured recipe through the public market-proxy /backtest edge
 * function (anon key, same boundary the browser uses) and writes the results
 * verbatim. The numbers are the engine's actual output — never hand-tuned.
 * To change which strategies/symbols are featured, edit COMBOS below and re-run.
 */
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const OUT = resolve(ROOT, "src/lib/landingSnapshot.ts");

// --- env (VITE_SUPABASE_URL + anon key) from .env ---------------------------
function readEnv(name) {
  const env = readFileSync(resolve(ROOT, ".env"), "utf8");
  const line = env.split("\n").find((l) => l.startsWith(`${name}=`));
  if (!line) throw new Error(`Missing ${name} in .env`);
  return line.slice(name.length + 1).trim().replace(/^["']|["']$/g, "");
}
const URL = readEnv("VITE_SUPABASE_URL");
const KEY = readEnv("VITE_SUPABASE_PUBLISHABLE_KEY");

// --- recipe components (mirror src/lib/backtestRecipes.ts) ------------------
const RECIPES = {
  golden_cross_20_50: {
    setup: { type: "EXPRESSION", timeframe: "1d", expression: { op: "CROSS_ABOVE", left: { indicator: "SMA", params: { period: 20 } }, right: { indicator: "SMA", params: { period: 50 } } } },
    exit: { type: "STOP_LOSS_PCT", timeframe: "1d", value: 0.05 },
  },
  ema_8_13_cross: {
    setup: { type: "EXPRESSION", timeframe: "1d", expression: { op: "CROSS_ABOVE", left: { indicator: "EMA", params: { period: 8 } }, right: { indicator: "EMA", params: { period: 13 } } } },
    exit: { type: "CONDITIONAL_OR_FIXED", timeframe: "1d", conditions: [{ type: "STOP_LOSS_PCT", value: 0.04 }, { type: "TAKE_PROFIT_PCT", value: 0.15 }] },
  },
  rsi_oversold_bounce: {
    setup: { type: "INDICATOR_THRESHOLD", timeframe: "1d", indicator: "RSI", params: { period: 14 }, operator: "<", value: 20 },
    trigger: { type: "CANDLE_PATTERN", timeframe: "1d", pattern: "GREEN_CANDLE" },
    exit: { type: "STOP_LOSS_PCT", timeframe: "1d", value: 0.05 },
  },
};

const START = "2021-01-01";
const END = "2024-12-31";
const PERIOD = "2021–2024";

// `featured` powers the pinned "Test your idea" story card; the rest power the
// TradlyteSelect strategy preview cards (keyed by their TradlyteSelect id).
const COMBOS = [
  { key: "featured", title: "Fast EMA Cross (8 / 13)", strategy: "ema_8_13_cross", symbol: "NVDA" },
  { key: "momentum", title: "Momentum Rider", strategy: "ema_8_13_cross", symbol: "AMD" },
  { key: "golden", title: "Golden Cross", strategy: "golden_cross_20_50", symbol: "GOOG" },
  { key: "mean", title: "Mean Reversion", strategy: "rsi_oversold_bounce", symbol: "NVDA" },
];

async function runBacktest(c) {
  const body = {
    strategy_name: c.strategy, symbol: c.symbol, timeframe: "1d",
    start_date: START, end_date: END, initial_capital: 10000,
    components: RECIPES[c.strategy],
  };
  const res = await fetch(`${URL}/functions/v1/market-proxy?path=/backtest`, {
    method: "POST",
    headers: { apikey: KEY, Authorization: `Bearer ${KEY}`, "Content-Type": "application/json" },
    body: JSON.stringify(body),
  });
  if (!res.ok) throw new Error(`${c.key} ${c.symbol}: HTTP ${res.status}`);
  const json = await res.json();
  const d = json.data;
  if (!d || typeof d.total_return_pct !== "number") {
    throw new Error(`${c.key} ${c.symbol}: no result (${JSON.stringify(json).slice(0, 160)})`);
  }
  const round = (n, p = 4) => Number(n.toFixed(p));
  return {
    key: c.key, strategyTitle: c.title, strategyId: c.strategy, symbol: c.symbol,
    startDate: START, endDate: END, periodLabel: PERIOD,
    totalReturnPct: round(d.total_return_pct), winRate: round(d.win_rate ?? 0, 3),
    totalTrades: d.total_trades ?? 0,
  };
}

const asOf = new Date().toISOString().slice(0, 10);
const results = [];
for (const c of COMBOS) {
  process.stdout.write(`  backtesting ${c.title} (${c.symbol})… `);
  const r = await runBacktest(c);
  console.log(`${(r.totalReturnPct * 100).toFixed(1)}%  (${r.totalTrades} trades)`);
  results.push(r);
}

const featured = results.find((r) => r.key === "featured");
const others = results.filter((r) => r.key !== "featured");
const fmt = (o) => JSON.stringify(o, null, 2).replace(/\n/g, "\n");

const file = `/**
 * AUTO-GENERATED by scripts/generate-landing-snapshot.mjs — do not edit by hand.
 * Real Strategy Lab backtest results, snapshotted for the marketing landing page.
 * Regenerate:  npm run snapshot:landing
 */
export interface BacktestSnapshot {
  key: string;
  strategyTitle: string;
  strategyId: string;
  symbol: string;
  startDate: string;
  endDate: string;
  periodLabel: string;
  /** Fraction, e.g. 1.654 = +165.4% (engine field data.total_return_pct). */
  totalReturnPct: number;
  /** Fraction, e.g. 0.478 = 48% (engine field data.win_rate). */
  winRate: number;
  totalTrades: number;
}

/** Date this snapshot was last regenerated (YYYY-MM-DD). */
export const SNAPSHOT_AS_OF = ${JSON.stringify(asOf)};

/** Featured result for the pinned "Test your idea" story card. */
export const LANDING_BACKTEST: BacktestSnapshot = ${fmt(featured)};

/** Strategy preview cards (TradlyteSelect), keyed by its card id. */
export const FEATURED_STRATEGIES: BacktestSnapshot[] = ${fmt(others)};
`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, file);
console.log(`\n✓ wrote ${OUT} (as of ${asOf})`);
